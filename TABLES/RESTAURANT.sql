
CREATE TABLE FOOD_SCHEDULE(
ID INT PRIMARY KEY,
TYPE VARCHAR(50) NOT NULL,
FROM_TIME TIME NOT NULL,
TO_TIME TIME NOT NULL
); 

CREATE TABLE MENU_ITEMS( 
ID INT PRIMARY KEY AUTO_INCREMENT,
ITEMS VARCHAR(50) NOT NULL,
RATE INT NOT NULL
);

CREATE TABLE ITEM_SCHEDULES(
ID INT PRIMARY KEY AUTO_INCREMENT,
FOOD_TYPE_ID INT,
ITEM_NAME VARCHAR(200)NOT NULL,
REMAINING_QUANTITY INT NOT NULL,
CONSTRAINT fk_ID FOREIGN KEY (FOOD_TYPE_ID) REFERENCES FOOD_SCHEDULE(ID)
);  
 
CREATE TABLE ORDER_RECORDS(
ID INT PRIMARY KEY AUTO_INCREMENT NOT NULL,
ORDER_ID INT  NOT NULL,
SEATNO VARCHAR(60) NOT NULL,
ITEM VARCHAR(50) NOT NULL,
QUANTITY INT NOT NULL,
STATUS VARCHAR(60)NOT NULL,
ORDERDATE  TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);
 
CREATE TABLE SEED_SEAT(
ID INT PRIMARY KEY AUTO_INCREMENT,
SEATNO VARCHAR(80)NOT NULL,
STATUS VARCHAR(42) DEFAULT 'AVAILABLE'
);

CREATE TABLE MAX_LIMIT(
ID INT PRIMARY KEY NOT NULL ,
MAX_ORDER INT NOT NULL 
); 

CREATE TABLE BILL_DETAIL(
ID INT PRIMARY KEY AUTO_INCREMENT,
ORDER_ID INT NOT NULL,
ITEM VARCHAR(90)NOT NULL,
PRICE INT NOT NULL
);

